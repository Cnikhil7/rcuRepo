public class Contacts {

    private String contactName;

    private String contactId;

    private String contactPhone;

    private String contactTimesContacted;

    public String getContactName() {
        return contactName;
    }

    public void setContactName(String contactName) {
        this.contactName = contactName;
    }

    public String getContactId() {
        return contactId;
    }

    public void setContactId(String contactId) {
        this.contactId = contactId;
    }

    public String getContactPhone() {
        return contactPhone;
    }

    public void setContactPhone(String contactPhone) {
        this.contactPhone = contactPhone;
    }

    public String getContactTimesContacted() {
        return contactTimesContacted;
    }

    public void setContactTimesContacted(String contactTimesContacted) {
        this.contactTimesContacted = contactTimesContacted;
    }
}
